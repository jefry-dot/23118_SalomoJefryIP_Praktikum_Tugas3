
public class MatematikaInheritance {
    public static void main(String[] args) {
        Matematika2 mat = new Matematika2();

        System.out.println(mat.tambah(10, 2));
        System.out.println(mat.kurang(10, 2));
        System.out.println(mat.kali(10, 2));
        System.out.println(mat.bagi(10, 2));
        System.out.println(mat.modulus(10, 2));
    }
}
